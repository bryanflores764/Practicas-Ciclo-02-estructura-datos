# Ejercicio 5: Simulación de registro de un estudiante universitario

# Variables del estudiante
nombre = "Ana Martínez"  # str
edad = 19  # int
promedio = 8.7  # float
becado = True  # bool

# Mostrar la información del estudiante
print("=== Registro de estudiante ===")
print(f"Nombre: {nombre}")
print(f"Edad: {edad} años")
print(f"Promedio: {promedio}")
print(f"Becado: {becado}")

# Cálculos con las variables numéricas
print("\n=== Cálculos ===")
print(f"Edad en 5 años: {edad + 5}")  # Suma con int
print(f"Promedio 1 punto extra: {promedio + 1}")  # Suma con float
print(f"Promedio multiplicado por 2: {promedio * 2}")  # Multiplicación con float